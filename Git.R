#Cell Biology Lab Worksheet Solutions**
 ## Prelab Activity 3.1: Basic Definitions
  
  **Cytoplasm**: Jelly-like substance inside cell membrane containing organelles and cytosol.[1]
**Cytosol**: Fluid portion of cytoplasm (water + dissolved substances, excluding organelles).[1]
**Organelles**: Membrane-bound structures with specialized functions inside eukaryotic cells.[1]

## Organelle Definitions (3.6.0.1)

**Rough Endoplasmic Reticulum**: Protein synthesis (ribosomes attached); folds/modifies proteins.[1]
**Smooth Endoplasmic Reticulum**: Lipid synthesis, detoxification, calcium storage.[1]
**Golgi apparatus**: Modifies, packages, sorts proteins/lipids for secretion/transport.[1]
**Mitochondrion**: ATP production via cellular respiration.[1]
**Lysosomes**: Digestion of macromolecules, autophagy, waste breakdown.[1]
**Peroxisomes**: Break down hydrogen peroxide, fatty acid oxidation.[1]
**Microfilaments**: Actin fibers; cell shape, movement, cytokinesis.[1]
**Microtubules**: Structural support, intracellular transport, mitosis spindle.[1]
**Cilia**: Short hair-like projections; move fluid/mucus across cell surface.[1]
**Flagella**: Long whip-like projection; cell motility (sperm).[1]
**Nucleus**: DNA storage, gene expression control, RNA synthesis.[1]
**Nucleolus**: Ribosome subunit assembly site.[1]

## Membrane Transport Definitions

**Passive Transport**: Movement down concentration gradient (no energy needed).[1]
**Active Transport**: Movement against concentration gradient (requires ATP).[1]
**Diffusion**: Passive movement of molecules from high to low concentration.[1]
**Osmosis**: Passive diffusion of water across semipermeable membrane.[1]

## Cell Cycle Definitions (Prelab 3.5)

**Cytokinesis**: Division of cytoplasm following mitosis.[1]
**Interphase**: G1 + S + G2 (cell growth, DNA replication).[1]
**M-phase**: Mitosis + cytokinesis (nuclear/cytoplasmic division).[1]
**Chromosome**: Condensed chromatin visible during cell division.[1]
**Chromatin**: DNA + histone proteins (uncondensed form).[1]

**Mitosis Stages**: Prophase → Prometaphase → Metaphase → Anaphase → Telophase.[1]

## Table 3.1: Mitosis Stages (Prelab 3.2)

